# Learning about Data Types

# Data Type 1 - Integer or int
age = 26
print("\nData Type 1")
print("Your age is: ", age)
print("Data type of age variable is:", type(age))
# print(len(age)) --- Integers do not have len function

# Data Type 2 - Float
height = 176.54
print("\nData Type 2")
print("Your height is: ", height)
print("Data type of age variable is:", type(height))
# print(len(height)) --- Float also do not have len function

# Data Type 3 - String or str
name = "Mohammad Wasiq Khan"
print("\nData Type 3")
print("Your name is: ", name)
print("Data type of name variable is:", type(name))
print("Number of characters in your name is: ", len(name))

# Data Type 4 - Boolean or bool
is_student = True
print("\nData Type 4")
print("You are a student: ", is_student)
print("Data type of is_student variable is:", type(is_student))
# print(len(is_student)) --- Boolean also do not have len function

# Data Type 5 - None/empty 
have_a_job = None
print("\nData Type 5")
print("You have a Job: ", have_a_job)
print("Data type of have_a_job variable is:", type(have_a_job))
# print(len(have_a_job)) --- None/empty also do not have len function