#Learning about varibales

#Consider you have to input following lines of code
print("Hi! My name is Wasiq")
print("Wasiq is learning Python Programming Language.")
print("Wasiq wants to become a Data Engineer!\n")

# Now if we need to change the name from Wasiq to Waris then we have to do it manually in each line of code.
# And if there is 1000 lines of code just like in real world projects than it will be tedious and impossible task. 
# So, here comes the use of variables
name = "Wasiq"
print("Hi! My name is " + name)
print(name + " is learning Python Programming Language.")
print(name +" wants to become a Data Engineer!\n")

# Now if we need to change the name then we can do it by just changing the value assigned to variable "name"
name = "Tom Holand"
print("Hi! My name is " + name)
print(name + " is learning Python Programming Language.")
print(name +" wants to become a Data Engineer!\n")

# This makes varibles very important part of any programming language